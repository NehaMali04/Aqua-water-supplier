// ===== LIGHTBOX =====
function openLightbox(src) {
  document.getElementById('lb-img').src = src;
  document.getElementById('lightbox').classList.add('active');
  document.body.style.overflow = 'hidden';
}
function closeLightbox() {
  document.getElementById('lightbox').classList.remove('active');
  document.body.style.overflow = '';
}
document.addEventListener('keydown', e => { if (e.key === 'Escape') closeLightbox(); });

// ===== FORM SUBMIT =====
function submitForm(e) {
  e.preventDefault();
  const btn = e.target.querySelector('button[type="submit"] span');
  const isEn = document.getElementById('btn-en').classList.contains('active');
  btn.textContent = isEn ? 'Order Sent! ✓' : 'ऑर्डर पाठवली! ✓';
  e.target.querySelector('button').style.background = '#28a745';
  setTimeout(() => {
    btn.textContent = isEn ? 'Send Order' : 'ऑर्डर पाठवा';
    e.target.querySelector('button').style.background = '';
    e.target.reset();
  }, 3000);
}

// ===== SCROLL REVEAL =====
const observer = new IntersectionObserver(entries => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      entry.target.style.opacity = '1';
      entry.target.style.transform = 'translateY(0)';
    }
  });
}, { threshold: 0.1 });
document.querySelectorAll('.service-card,.why-card,.gallery-item').forEach(el => {
  el.style.opacity = '0';
  el.style.transform = 'translateY(28px)';
  el.style.transition = 'opacity 0.5s ease, transform 0.5s ease';
  observer.observe(el);
});
